int : 정수
  int a = 1;
double : 부동 소수점 숫자
  double b = 1.0;
String : 문자열
  String c = "Hello Flutter";
bool : True/False
  bool d = false;
List : 리스트
  List<int> e = [1,2,3,4];
Set(집합) : 중복을 허용하지 않으며, 순서가 없는 컬렉션 자료구
  Set<int> f = [1,2,3,2]; #중복 비허용이므로 f = [1,2,3];
Map(맵) : 키-값 쌍으로 이루어진 자료구
  Map<String,  int> g = {
    'lee' : 10,
    'jun' : 5,
    'won' : 1,
  };
